    private void SalvarbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarbtActionPerformed
        Editora editora = new Editora();
        editora.setNome(EditoraNometxt.getText());
        editora.setMunicipio(EditoraMunicipiotxt.getText());
        try {
            if (mode.equals("INS")) {
                editoraDAO.save(editora);
            } else if (mode.equals("UPD")) {
                editora.setEditora_id(Integer.parseInt(EditoraIDtxt.getText()));
                editoraDAO.update(editora);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        listar();
    }//GEN-LAST:event_SalvarbtActionPerformed

