    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        int selected = tabela.getSelectedRow();
        EditoraIDtxt.setText(tabela.getValueAt(selected, 0).toString());
        EditoraNometxt.setText(tabela.getValueAt(selected, 1).toString());
        EditoraMunicipiotxt.setText(tabela.getValueAt(selected, 2).toString());
        this.mode = "UPD";
    }//GEN-LAST:event_tabelaMouseClicked

    private void NovobtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NovobtActionPerformed
        EditoraIDtxt.setText("");
        EditoraNometxt.setText("");
        EditoraMunicipiotxt.setText("");
        this.mode = "INS";
    }//GEN-LAST:event_NovobtActionPerformed

    private void RemoverbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoverbtActionPerformed
        int opt = JOptionPane.showConfirmDialog(this, "Excluir registro?");
        if (opt == JOptionPane.YES_OPTION) {
            Editora editora = new Editora();
            editora.setEditora_id(Integer.parseInt(EditoraIDtxt.getText()));

            try {
                editoraDAO.delete(editora);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        EditoraIDtxt.setText("");
        EditoraNometxt.setText("");
        EditoraMunicipiotxt.setText("");
        this.mode = "INS";
        listar();
        }
    }//GEN-LAST:event_RemoverbtActionPerformed

     public void listar() {
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        model.setNumRows(0);
        try {
            for (Editora editora : editoraDAO.findAll()) {
                String linha[] = {"" + editora.getEditora_id(), editora.getNome(), editora.getMunicipio()};
                model.addRow(linha);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
